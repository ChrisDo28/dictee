<h3>1. Regels dictee</h3>
<ol>
<li>Gebruik van hoofdletters en plaatsing van trema's wordt naast de spelling ook beoordeeld in het dictee.</li>
<li>Getallen dienen conform de gebruikelijke richtlijnen uitgeschreven te worden. <a href="https://www.onzetaal.nl/taalloket/getallen-in-letters-of-cijfers" target="_blank">Meer informatie</a></li>
</ol>
<h3>2. Regels kandidaat</h3>
<ol>
<li>Het is verplicht om het dictee af te leggen met een door Kwabbel, Inc. erkende examinator.</li>
<li>Het is verplicht om het dictee af te leggen op een door N&amp;M Games goedgekeurd apparaat.</li>
<li>Het is verplicht om eerlijk je voor- én achternaam op te geven.</li>
<li>Het is verplicht om de browser waarin het dictee wordt afgenomen in volledig scherm te zetten.</li>
<li>Het is niet toegestaan om af te kijken of te spieken bij andere deelnemers.</li>
<li>Het is niet toegestaan om spellingscontrole te gebruiken.</li>
<li>Het is niet toegestaan om hulp te vragen van een ander, ongeacht of deze deelneemt aan het dictee of niet.</li>
<li>Het is niet toegestaan om antwoorden aan andere DJO'ers kenbaar te maken.</li>
<li>Het is niet toegestaan om de browserconsole, de inspecteur of iets van dergelijke aard te gebruiken tijdens het dictee.</li>
<li>Het is niet toegestaan om onnodig oogcontact te maken met surveillanten, examinatoren en voorbijgangers.</li>
<li>Het is niet toegestaan om resultaten van het dictee op enigerlei wijze te kopiëren.</li>
<li>Het is niet toegestaan om eenzelfde dictee meer dan één keer af te leggen.</li>
<li>Het is niet toegestaan om te praten met andere deelnemers, surveillanten of examinatoren.</li>
</ol>
<h3>3. Regels examinator</h3>
<ol>
<li>De examinator dient de tekst van het dictee in een rustig tempo te doorlopen.</li>
<li>De examinator dient de tekst op verstaanbare wijze aan de kandidaten over te brengen.</li>
<li>De examinator dient na het uitspreken van een woord dat de kandidaten moeten spellen een kleine pauze te nemen, alvorens verder te gaan met voorlezen.</li>
<li>De examinator dient een woord slechts eenmalig uit te spreken. Indien een kandidaat om herhaling vraagt, dient dit opgevat te worden als een overtreding van artikel 2.12 van dit reglement.</li>
<li>Het is de examinator niet toegestaan om definities te geven van de woorden.</li>
<li>De examinator is er verantwoordelijk voor dat kandidaten en omstanders zichzelf geen inzage verschaffen tot de antwoorden.</li>
</ol>
<h3>4. Zak-/slaagregeling</h3>
<ol>
<li>Een kandidaat is geslaagd als deze ten minste de helft van de woorden goed gespeld en geformuleerd heeft.</li>
<li>Als een kandidaat is geslaagd, ontvangt deze het DJO Dictee-certificaat van zijn/haar examinator.</li>
<li>Indien het dictee in groepsverband is afgenomen, kan de examinator aan de kandidaat met de meeste goede antwoorden
een extra prijs uitreiken, ongeacht of deze geslaagd of gezakt is.</li>
</ol>
<h3>5. Overtredingen</h3>
<ol>
<li>Bij de eerste overtreding van het reglement gaat er een waarschuwing uit naar de overtreder.</li>
<li>Bij de tweede overtreding dient de overtreder te worden verwijderd uit het dictee.</li>
</ol>
