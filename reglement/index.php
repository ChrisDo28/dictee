        <h3>I. Algemene Spelregels</h3>
        <ul>
            <li>Spelling en Notatie
                <ul>
                    <li>Correct gebruik van hoofdletters, trema's en interpunctie wordt naast de spelling beoordeeld.</li>
                    <li>Getallen dienen conform de gebruikelijke richtlijnen te worden uitgeschreven.</li>
                </ul>
            </li>
        </ul>

        <h3>II. Voorwaarden voor Deelnemers</h3>
        <ul>
            <li>Deelnamevereisten
                <ul>
                    <li>Het dictee moet worden afgenomen onder toezicht van een erkende examinator.</li>
                    <li>Gebruik van een door N&amp;M Games goedgekeurd apparaat is verplicht.</li>
                    <li>De deelnemer moet eerlijk zijn/haar volledige naam verstrekken.</li>
                    <li>Het dictee moet in volledig schermmodus worden afgelegd in de browser.</li>
                </ul>
            </li>
            <li>Verboden Handelingen
                <ul>
                    <li>Geen spieken of afkijken bij andere deelnemers.</li>
                    <li>Geen gebruik van spellingscontrole.</li>
                    <li>Geen hulp vragen aan anderen, ongeacht deelname aan het dictee.</li>
                    <li>Geen bekendmaking van antwoorden aan andere deelnemers.</li>
                    <li>Geen gebruik van browserfuncties zoals de console of inspectietools.</li>
                    <li>Vermijd onnodig oogcontact met toezichthouders.</li>
                    <li>Geen duplicatie of kopiëren van dictee-uitslagen.</li>
                    <li>Slechts één keer deelnemen aan hetzelfde dictee.</li>
                    <li>Geen communicatie met andere deelnemers, toezichthouders of examinatoren.</li>
                </ul>
            </li>
        </ul>

        <h3>III. Taken van de Examinator</h3>
        <ul>
            <li>Leesproces
                <ul>
                    <li>De examinator moet de tekst van het dictee in een rustig tempo voorlezen.</li>
                    <li>Duidelijke en begrijpelijke uitspraak van de woorden naar de deelnemers toe.</li>
                    <li>Een korte pauze nemen na het spellen van een woord voordat verder wordt gegaan met voorlezen.</li>
                </ul>
            </li>
            <li>Herhaling van Woorden
                <ul>
                    <li>Een woord wordt slechts één keer uitgesproken.</li>
                </ul>
            </li>
            <li>Verboden Acties
                <ul>
                    <li>Geen verstrekking van definities van woorden aan de deelnemers.</li>
                    <li>Voorkomen dat deelnemers of omstanders toegang krijgen tot de antwoorden.</li>
                </ul>
            </li>
        </ul>

        <h3>IV. Beoordeling en Certificering</h3>
        <ul>
            <li>Slaagcriteria
                <ul>
                    <li>Een kandidaat is geslaagd als deze ten minste de helft van de woorden goed gespeld en geformuleerd heeft.</li>
                    <li>Als een kandidaat is geslaagd, ontvangt deze het DJO Dictee-certificaat van zijn/haar examinator.</li>
                </ul>
            </li>
            <li>Erkenning van Uitblinkers
                <ul>
                    <li>De examinator kan een extra prijs toekennen aan de deelnemer met de meeste correcte antwoorden in een groepsdictee.</li>
                </ul>
            </li>
        </ul>

        <h3>V. Sancties bij Overtredingen</h3>
        <ul>
            <li>Waarschuwingen en Verwijdering
                <ul>
                    <li>Eerste overtreding leidt tot een waarschuwing aan de overtreder.</li>
                    <li>Bij herhaalde overtreding wordt de overtreder uit het dictee verwijderd.</li>
                </ul>
            </li>
        </ul>
